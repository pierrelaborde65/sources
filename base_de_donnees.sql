DROP TRIGGER IF EXISTS `verif_desinscription`;
DELIMITER //
CREATE TRIGGER `verif_desinscription` BEFORE DELETE ON `inscription`
 FOR EACH ROW BEGIN
DECLARE places INT;
DECLARE points INT;
DECLARE date_event date;
DECLARE date_actuelle date;
 
select places_dispo into places from evenement where old.num_evenement = num_evenement;
select nombre_points into points from evenement where old.num_evenement = num_evenement;
select date_evenement into date_event from evenement where old.num_evenement = num_evenement;
select DATE( NOW() ) into date_actuelle;

IF date_event < date_actuelle then	
    SIGNAL SQLSTATE '45000'
	SET MESSAGE_TEXT = 'Désinscription impossible ! Cet événement est déjà passé !';

ELSE
	UPDATE evenement 
    SET places_dispo = places_dispo+1 WHERE old.num_evenement = num_evenement;
    UPDATE etudiant
    SET nb_points_total = nb_points_total-points WHERE old.num_etudiant = num_etudiant;

END IF;
END
