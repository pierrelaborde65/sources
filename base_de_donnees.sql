DELIMITER ;
DROP TRIGGER IF EXISTS `verif_inscription`;
DELIMITER //
CREATE TRIGGER `verif_inscription` BEFORE INSERT ON `inscription`
 FOR EACH ROW BEGIN
DECLARE moyenne FLOAT; 
DECLARE places INT;
DECLARE points INT;
DECLARE nb_etudiant INT;
DECLARE date_event date;
DECLARE date_actuelle date;

SELECT moyenne_etudiant INTO moyenne FROM etudiant WHERE num_etudiant = NEW.num_etudiant; 
select places_dispo into places from evenement where num_evenement = NEW.num_evenement;
select nombre_points into points from evenement where num_evenement = NEW.num_evenement;
select count(*) into nb_etudiant from inscription where num_evenement = NEW.num_evenement AND num_etudiant = NEW.num_etudiant;
select date_evenement into date_event from evenement where num_evenement = new.num_evenement;
select DATE( NOW() ) into date_actuelle;

IF date_event < date_actuelle then	
    SIGNAL SQLSTATE '45000'
	SET MESSAGE_TEXT = 'Inscription impossible ! Cet événement est déjà passé, il va falloir attendre lannée prochaine !';
    
ELSE IF moyenne < 10 THEN 
	SIGNAL SQLSTATE '45000'
	SET MESSAGE_TEXT = 'Inscription impossible ! Vous devez avoir une moyenne supérieure à 10 pour pouvoir vous inscrire à un événement !';

ELSE IF nb_etudiant > 0 THEN 
	SIGNAL SQLSTATE '45000'
	SET MESSAGE_TEXT = 'Inscription impossible ! Vous êtes déjà inscrit à cet événement !';

ELSE IF places <= 0 then	
    SIGNAL SQLSTATE '45000'
	SET MESSAGE_TEXT = 'Inscription impossible ! Plus de places disponibles pour cet événement !';

ELSE
	UPDATE evenement 
    SET places_dispo = places_dispo-1 WHERE num_evenement = NEW.num_evenement;
    UPDATE etudiant
    SET nb_points_total = nb_points_total+points WHERE num_etudiant = NEW.num_etudiant;

END IF;
END IF;
END IF;
END IF;
END
